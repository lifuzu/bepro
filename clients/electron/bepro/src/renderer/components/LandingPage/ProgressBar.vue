<template>
  <div>
    <Progress :percent="percent"></Progress>
    <ButtonGroup size="large">
      <Button icon="ios-plus-empty" @click="add"></Button>
      <Button icon="ios-minus-empty" @click="minus"></Button>
    </ButtonGroup>
  </div>
</template>
<script>
export default {
  data () {
    return {
      percent: 0
    }
  },
  methods: {
    add () {
      if (this.percent >= 100) {
        return false
      }
      this.percent += 10
    },
    minus () {
      if (this.percent <= 0) {
        return false
      }
      this.percent -= 10
    }
  }
}
</script>